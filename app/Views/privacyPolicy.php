        <p>
            Conformément à la réglementation en vigueur, vous disposez de droits concernant vos données personnelles, notamment le droit d’accès, de rectification ou de suppression. Vous pouvez nous contacter pour exercer ces droits.
        </p><br>

        <h2>Modification de la politique:</h2>
        <p>
            Cette politique de confidentialité peut être mise à jour à tout moment. Les modifications seront publiées sur cette page avec une date de mise à jour.
