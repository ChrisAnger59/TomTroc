        </p><br>

        <h2>Utilisation des données:</h2>
        <p>
            Les informations collectées sont utilisées pour :
        </p>
        <ul>
            <li>Fournir et améliorer nos services</li>
            <li>Personnaliser l’expérience utilisateur</li>
            <li>Communiquer avec les utilisateurs</li>
            <li>Assurer la sécurité et prévenir les fraudes</li>
        </ul><br>

        <h2>Partage des données:</h2>
        <p>
            Nous ne vendons ni ne louons vos données personnelles. Certaines informations peuvent être partagées avec des partenaires de confiance uniquement lorsque cela est nécessaire au fonctionnement du service ou pour répondre à des obligations légales.
        </p><br>

        <h2>Sécurité des données:</h2>
        <p>
            Nous mettons en place des mesures techniques et organisationnelles appropriées afin de protéger vos données contre tout accès, modification ou destruction non autorisés.
        </p><br>

